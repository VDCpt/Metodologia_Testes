function generateLocalLegalNarrative(analysis, lang) {
    const isPT = (lang === 'pt');

    const _totals    = (analysis && analysis.totals)    || {};
    const _crossings = (analysis && analysis.crossings) || {};

    const omissionPct = (_crossings.percentagemOmissao !== undefined)
        ? _crossings.percentagemOmissao
        : ((analysis && typeof analysis.omissionPct === 'number') ? analysis.omissionPct : 0);

    const btor = _totals.despesas         || analysis.btorLedger || 0;
    const btf  = _totals.faturaPlataforma || analysis.btfInvoice || 0;
    const discrepancy = _crossings.discrepanciaCritica || (btor - btf);
    const revenueGap  = _crossings.discrepanciaSaftVsDac7
        || ((_totals.saftBruto || 0) - (_totals.dac7TotalPeriodo || 0));
    const ivaFalta23  = _crossings.ivaFalta  || 0;
    const ivaFalta6   = _crossings.ivaFalta6 || 0;

    forensicLog('info', 'ENRICHMENT', '📄 A gerar narrativa de fallback local (offline)', {
        lang, omissionPct, btor, btf, discrepancy, revenueGap, ivaFalta23, ivaFalta6
    });

    if (isPT) {
        return `SÍNTESE JURÍDICA PERICIAL — MODO OFFLINE (DETERMINÍSTICO)

Com base na análise dos documentos fornecidos, apurou-se uma omissão de ${omissionPct.toFixed(2)}% entre as Despesas/Comissões retidas pelo extrato bancário (BTOR: ${window.formatForensicCurrency(btor)}) e o valor efetivamente faturado pela plataforma (BTF: ${window.formatForensicCurrency(btf)}), correspondente a um diferencial de ${window.formatForensicCurrency(discrepancy)}.

A triangulação SAF-T vs DAC7 revela uma omissão de receita declarada ao Estado de ${window.formatForensicCurrency(revenueGap)}. O IVA em falta apurado é de ${window.formatForensicCurrency(ivaFalta23)} (taxa de 23% — autoliquidação CIVA) e ${window.formatForensicCurrency(ivaFalta6)} (taxa de 6% — serviços de transporte).

Esta divergência sistemática configura indício de infração fiscal nos termos dos Art. 405.º e 406.º CC. Aplica-se a inversão do ónus da prova (Art. 344.º, n.º 2 CC) porquanto a plataforma detém o monopólio da emissão documental (Art. 36.º, n.º 11 CIVA), encontrando-se o sujeito passivo em situação de indefesa técnica.

Por conseguinte, impõe-se a aplicação do Princípio da Proximidade da Prova (Acórdão STJ 11/07/2013), cabendo em exclusivo à plataforma demonstrar a licitude das retenções apuradas.

Requer-se a procedência da presente ação e a condenação da requerida no pagamento de ${window.formatForensicCurrency(discrepancy)}, acrescida de juros moratórios à taxa legal supletiva (Art. 559.º CC) e custas processuais.`;
    } else {
        return `FORENSIC LEGAL SUMMARY — OFFLINE MODE (DETERMINISTIC)

Based on the analysis of the provided documents, an invoicing omission of ${omissionPct.toFixed(2)}% was identified between commissions withheld per bank statement (BTOR: ${window.formatForensicCurrency(btor)}) and the amount actually invoiced by the platform (BTF: ${window.formatForensicCurrency(btf)}), resulting in a differential of ${window.formatForensicCurrency(discrepancy)}.

The SAF-T vs DAC7 triangulation reveals an undeclared revenue gap of ${window.formatForensicCurrency(revenueGap)} reported to the Tax Authority. Missing VAT amounts to ${window.formatForensicCurrency(ivaFalta23)} (23% rate — VAT self-assessment) and ${window.formatForensicCurrency(ivaFalta6)} (6% rate — transportation services).

This systematic divergence constitutes evidence of tax infringement under Art. 405 and 406 of the Civil Code. The reversal of the burden of proof applies (Art. 344(2) CC) since the platform holds the monopoly on document issuance (Art. 36(11) CIVA), leaving the taxpayer in a position of technical defenselessness.

Under the Principle of Proximity of Evidence (STJ Judgment 07/11/2013), the burden of proof falls exclusively on the platform to demonstrate the lawfulness of the retained amounts.

The claimant requests that the action be upheld and the defendant be ordered to pay ${window.formatForensicCurrency(discrepancy)}, plus statutory default interest (Art. 559 CC) and court costs.`;
    }
}

